package jp.practice.java;

public class calc {

	public static void main(String[] args) {
		// 算術演算子 + - * / %
		int a = 15;
		int b = 4;
		
		// 足し算
		System.out.println(a + b);
		
		// 引き算
		System.out.println(a - b);
		
		// 掛け算
		System.out.println(a * b);
		
		// 割り算
		System.out.println(a / b);
		
		// 剰余
		System.out.println(a % b);
	}

}
